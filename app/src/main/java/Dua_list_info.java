public class Dua_list_info {
    String dua_Number;
    String dua_name;


    public Dua_list_info() {
    }

    public Dua_list_info(String dua_Number, String dua_name) {
        this.dua_Number = dua_Number;
        this.dua_name = dua_name;
    }

    public String getDua_Number() {
        return dua_Number;
    }

    public void setDua_Number(String dua_Number) {
        this.dua_Number = dua_Number;
    }

    public String getDua_name() {
        return dua_name;
    }

    public void setDua_name(String dua_name) {
        this.dua_name = dua_name;
    }
}
